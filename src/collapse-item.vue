<template>
    <div class="collapse-item">
        <div class="title" @click="toggleContent">{{title}}</div>
        <div class="content" v-if="open">
            <slot></slot>
        </div>
    </div>
</template>

<script>
    export default {
        name: "WheelCollapseItem",
        props:{
            title:{
                type:String,
                required: true
            }

        },
        data(){
            return {
                open:false
            }
        },
        inject:['eventbus'],
        methods:{
            toggleContent(){
                this.open=!this.open
                this.eventBus&&this.eventBus.$emit('update:selected', this.title)
            }
        },
        created() {
            this.eventBus&& this.eventBus.$on('update:selected',(title)=>{
                this.open=title === this.title
            })
        }
    }
</script>

